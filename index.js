import express from 'express';

/*
================================================================================
SETUP
================================================================================
*/
const app = express();
const PORT = process.env.PORT || 3001;

/*
================================================================================
ROUTES
================================================================================
*/
app.get('/', async (req, res) => {
  res.send('<h1>Hello, World!</h1>');
});

/*
================================================================================
LISTEN
================================================================================
*/
app.listen(PORT, () => {
  console.log(`Server running on port ${PORT}`);
});
